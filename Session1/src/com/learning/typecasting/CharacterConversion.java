package com.learning.typecasting;

public class CharacterConversion {
	public static void main(String[] args) {
		
		
		char cc='A';
		
		int a=cc;
		
		System.out.println("int is "+a);
		
		int gg=a+32;//97
		
		char cf=(char) gg;
		
		System.out.println("char is "+cf);
		
		//int char is compitible
		
		
	}

}
