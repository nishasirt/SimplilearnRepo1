package com.learning.session1;

public class Loops {
   public static void main(String[] args) {
	  int i=1;
	  System.out.println("While loop");	  
	  while(i<=5)
	  {
		  
		  System.out.println(i);
		  i++;
	  }
	  System.out.println("While loop");	
	  do{
		  System.out.println(i);
		  i++;
	  }while(i<=10);
	  
		  System.out.println("for loop");
		  
		  for( i=11;i<=15;i++)
		  {
			  System.out.println(i);
		  }
	  
	  
	  
}
}
